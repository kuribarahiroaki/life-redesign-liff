// ------------------------------------------------------------
// 判定ロジック（合計最多タイプ。同点はQ1の回答が指すタイプを優先）
// responses: 各設問で選んだ選択肢のindex（0〜3）の配列。長さ7。
// ------------------------------------------------------------
export function scoreAnswers(responses: number[]): {
  result_type: TypeKey;
  tag: string;
  name: string;
  scores: Scores;
} {
  const totals: Scores = { T1: 0, T2: 0, T3: 0, T4: 0 };
  responses.forEach((optIndex, qi) => {
    const opt = QUESTIONS[qi].options[optIndex];
    (Object.keys(totals) as TypeKey[]).forEach((k) => {
      totals[k] += opt.scores[k];
    });
  });

  const max = Math.max(...Object.values(totals));
  const top = (Object.keys(totals) as TypeKey[]).filter((k) => totals[k] === max);

  let result_type: TypeKey;
  if (top.length === 1) {
    result_type = top[0];
  } else {
    // 同点 → Q1で選んだ選択肢が指すタイプを優先
    const q1Opt = QUESTIONS[0].options[responses[0]];
    const q1Type = (Object.keys(q1Opt.scores) as TypeKey[]).find((k) => q1Opt.scores[k] > 0)!;
    result_type = top.includes(q1Type) ? q1Type : top[0];
  }

  return {
    result_type,
    tag: RESULT[result_type].tag,
    name: RESULT[result_type].name,
    scores: totals,
  };
}

